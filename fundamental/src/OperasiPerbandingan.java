public class OperasiPerbandingan {
  public static void main(String[] args) {
    int x = 10;
    int y = 20;
    // TODO: (==) <- Equel to (!=) <- Not equel to
    System.out.println(x > y); // False
    System.out.println(x < y); // True
    System.out.println(x >= y); // False
    System.out.println(x <= y); // True
    System.out.println(x == y); // False
    System.out.println(x != y); // True
  }
}

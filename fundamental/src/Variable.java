public class Variable {
  public static void main(String[] args) {
    String name;
    name = "MUH. AHSAN";

    System.out.println(name);

    int age = 30;

    System.out.println(age);

    String address = "Masamba";

    System.out.println(address);

    var firstName = "MUH.";
    var lastName = "AHSAN";

    System.out.println(firstName + "" + lastName);
  }
}
